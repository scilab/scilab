#include "warningmode.h"
/*--------------------------------------------------------------------------*/
BOOL WARNINGMODEON=TRUE;
/*--------------------------------------------------------------------------*/
BOOL setWarningMode(BOOL ON)
{
	WARNINGMODEON = ON;
	return WARNINGMODEON;
}
/*--------------------------------------------------------------------------*/
BOOL getWarningMode(void)
{
	return WARNINGMODEON;
}
/*--------------------------------------------------------------------------*/
int C2F(iswarningon)(int *rep)
{
	if (WARNINGMODEON) *rep=1;
	else *rep=0;
	return 0;
}
